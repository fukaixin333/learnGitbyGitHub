package com.citic.server.gf.domain.response;

import java.io.Serializable;
import java.util.List;

/**
 * 查询/控制请求涉及的证件信息列表
 * 
 * @author Liu Xuanfei
 * @date 2016年3月9日 下午6:53:31
 */
public class CertificateResponse implements Serializable {
	private static final long serialVersionUID = -2068843532880198117L;
	
	/** 错误信息描述 */
	private String errorMsg;
	
	/** 查询/控制请求涉及的证件信息列表 */
	private List<CertificateResponse_ZjInfo> zjInfoList;
	
	// ==========================================================================================
	//                     Getter and Setter
	// ==========================================================================================
	public String getErrorMsg() {
		return errorMsg;
	}
	
	public void setErrorMsg(String errorMsg) {
		this.errorMsg = errorMsg;
	}
	
	public List<CertificateResponse_ZjInfo> getZjInfoList() {
		return zjInfoList;
	}
	
	public void setZjInfoList(List<CertificateResponse_ZjInfo> zjInfoList) {
		this.zjInfoList = zjInfoList;
	}
}
