package com.citic.server.dx.domain;

import java.io.Serializable;

/**
 * 人工举报报文 - 交易明细
 * 
 * @author Liu Xuanfei
 * @date 2016年4月12日 下午3:50:37
 */
public class Transaction implements Serializable {
	private static final long serialVersionUID = -990344866778175797L;
	
	/** 交易流水号 */
	private String ID;
	
	/** 交易时间(yyyyMMddHHmmss) */
	private String time;
	
	/** 交易类型：根据各银行实际数据反馈 */
	private String type;
	
	/** 币种(CNY人民币、USD美元、EUR欧元…) */
	private String currency;
	
	/** 交易金额 */
	private String transferAmount;
	
	/** 转出账户所属银行机构编码 */
	private String transferOutBankID;
	
	/** 转出账户所属银行名称 */
	private String transferOutBankName;
	
	/** 转出账户名 */
	private String transferOutAccountName;
	
	/** 转出卡/折号 */
	private String transferOutAccountNumber;
	
	/** 交易对端账户所属银行机构编码 */
	private String transferInBankID;
	
	/** 交易对端账户所属银行名称 */
	private String transferInBankName;
	
	/** 交易对端账户名 */
	private String transferInAccountName;
	
	/** 交易对端卡/折号 */
	private String transferInAccountNumber;
	
	/** IP地址 */
	private String IP;
	
	/** MAC地址 */
	private String MAC;
	
	/** 设备ID */
	private String deviceID;
	
	/** 交易地点 */
	private String place;
	
	/** 备注 */
	private String remark;
	
	// ==========================================================================================
	//                     Helper Field
	// ==========================================================================================
	/** 借贷标志（0-借；1-贷） */
	private String borrowingSigns;
	
	// ==========================================================================================
	//                     Getter and Setter
	// ==========================================================================================
	public String getID() {
		return ID;
	}
	
	public void setID(String id) {
		ID = id;
	}
	
	public String getTime() {
		return time;
	}
	
	public void setTime(String time) {
		this.time = time;
	}
	
	public String getType() {
		return type;
	}
	
	public void setType(String type) {
		this.type = type;
	}
	
	public String getCurrency() {
		return currency;
	}
	
	public void setCurrency(String currency) {
		this.currency = currency;
	}
	
	public String getTransferAmount() {
		return transferAmount;
	}
	
	public void setTransferAmount(String transferAmount) {
		this.transferAmount = transferAmount;
	}
	
	public String getTransferOutBankID() {
		return transferOutBankID;
	}
	
	public void setTransferOutBankID(String transferOutBankID) {
		this.transferOutBankID = transferOutBankID;
	}
	
	public String getTransferOutBankName() {
		return transferOutBankName;
	}
	
	public void setTransferOutBankName(String transferOutBankName) {
		this.transferOutBankName = transferOutBankName;
	}
	
	public String getTransferOutAccountName() {
		return transferOutAccountName;
	}
	
	public void setTransferOutAccountName(String transferOutAccountName) {
		this.transferOutAccountName = transferOutAccountName;
	}
	
	public String getTransferOutAccountNumber() {
		return transferOutAccountNumber;
	}
	
	public void setTransferOutAccountNumber(String transferOutAccountNumber) {
		this.transferOutAccountNumber = transferOutAccountNumber;
	}
	
	public String getTransferInBankID() {
		return transferInBankID;
	}
	
	public void setTransferInBankID(String transferInBankID) {
		this.transferInBankID = transferInBankID;
	}
	
	public String getTransferInBankName() {
		return transferInBankName;
	}
	
	public void setTransferInBankName(String transferInBankName) {
		this.transferInBankName = transferInBankName;
	}
	
	public String getTransferInAccountName() {
		return transferInAccountName;
	}
	
	public void setTransferInAccountName(String transferInAccountName) {
		this.transferInAccountName = transferInAccountName;
	}
	
	public String getTransferInAccountNumber() {
		return transferInAccountNumber;
	}
	
	public void setTransferInAccountNumber(String transferInAccountNumber) {
		this.transferInAccountNumber = transferInAccountNumber;
	}
	
	public String getIP() {
		return IP;
	}
	
	public void setIP(String ip) {
		IP = ip;
	}
	
	public String getMAC() {
		return MAC;
	}
	
	public void setMAC(String mac) {
		MAC = mac;
	}
	
	public String getDeviceID() {
		return deviceID;
	}
	
	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}
	
	public String getPlace() {
		return place;
	}
	
	public void setPlace(String place) {
		this.place = place;
	}
	
	public String getRemark() {
		return remark;
	}
	
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	public String getBorrowingSigns() {
		return borrowingSigns;
	}
	
	public void setBorrowingSigns(String borrowingSigns) {
		this.borrowingSigns = borrowingSigns;
	}
}
