package com.citic.server.inner.domain.request;

import com.citic.server.inner.domain.RequestMessage;

/**
 * 公司同业客户信息查询
 * 
 * @author liuxuanfei
 * @date 2016年12月23日 下午4:45:21
 */
public class Input025327 extends RequestMessage {
	private static final long serialVersionUID = -209165524826513962L;
	
	private String idType; // 开户证件类型
	private String idNumber; // 开户证件号码
	private String customerName; // 开户名称
	private String customerNumber; // 客户编号
	private String accountNumber; // 账号/合约号
	
	public Input025327() {
	}
	
	public Input025327(String accountNumber) {
		this.accountNumber = accountNumber;
	}
	
	public Input025327(String idType, String idNumber, String customerName) {
		this.idType = idType;
		this.idNumber = idNumber;
		this.customerName = customerName;
	}
	
	public String getIdType() {
		return idType;
	}
	
	public void setIdType(String idType) {
		this.idType = idType;
	}
	
	public String getIdNumber() {
		return idNumber;
	}
	
	public void setIdNumber(String idNumber) {
		this.idNumber = idNumber;
	}
	
	public String getCustomerName() {
		return customerName;
	}
	
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	
	public String getCustomerNumber() {
		return customerNumber;
	}
	
	public void setCustomerNumber(String customerNumber) {
		this.customerNumber = customerNumber;
	}
	
	public String getAccountNumber() {
		return accountNumber;
	}
	
	public void setAccountNumber(String accountNumber) {
		this.accountNumber = accountNumber;
	}
	
}
